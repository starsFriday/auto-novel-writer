“罢了，看在是个绝世大美女，还能帮本仙尊突破的份上，这‘软饭’本仙尊就勉为其难地吃了吧。”林逸嘿嘿一笑。

正当林逸准备起身去认亲时，他的眉头突然微微一挑，目光落在了那辆奢华的紫金罽车上。

在凡人看不见的视野里，方圆百丈的气流开始诡异地凝结。

望江楼的屋顶上、街角密不透风的阴影里，不知何时已经潜伏了十几道散发着冰冷杀气的身影。

