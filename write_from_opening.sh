林逸嘴里嘟囔着，顺手擦了擦嘴角的汤汁。

谁能想到，这个看似穷酸的落魄道士，体内却运转着足以惊世骇俗的“太古吞天诀”。就在下山前的那个晚上，他因为嫌后山的护山黑蛟太吵，一巴掌把那条修行了五百年的妖蛟拍成了残废，顺便抽了龙筋当裤腰带。

“死老头子，说什么红尘历练，非要我下山当什么劳什子郡主保镖。”

林逸从怀里摸出一张皱巴巴的圣旨和一张画像。画像上的女子清冷孤傲，正是车轿中的萧梦瑶。

