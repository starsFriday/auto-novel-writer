“轰——！”

一声巨响毫无征兆地爆开，望江楼二楼的窗户瞬间碎裂，无数道裹挟着雄浑内劲的毒针如暴雨般朝马车倾泻而去。

紧接着，三名手持墨黑长刀、太阳穴高高鼓起的蒙面刺客凌空跃起，长刀带起数丈长的凌厉刀芒，直劈马车中的萧梦瑶！

“有刺客！保护郡主！”羽林卫顿时大乱。

萧梦瑶掀开车帘，看着那迎面劈来的恐怖刀芒，俏脸一片惨白，眼中闪过一丝绝望。

就在这千钧一发之际，一根吃剩的竹筷，带着刺耳的破空之声，无声无息地从面摊方向激射而来……
