大景朝，天德三年。

京城盛平，十里御街繁华如锦。

一辆由四匹纯白骏马套着的紫金齐头罽车，在数十名顶盔贯甲、腰悬横刀的羽林卫护送下，浩浩荡荡地停在了京城最豪奢的酒楼——望江楼前。

车帘微启，露出一张倾国倾城的容颜。那女子年方十八，云鬓峨峨，肤如凝脂，一双凤眸冷若寒霜，周身透着一股高不可攀的皇家贵气。

