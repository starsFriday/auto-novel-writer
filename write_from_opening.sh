fi

# 规划总章节数。
TARGET_CHAPTERS="${TARGET_CHAPTERS:-20}"

# 每章目标中文字符数。
TARGET_CHARS="${TARGET_CHARS:-5500}"

# 如果没有 config.local.json，就使用 config.example.json。
if [[ ! -f "$CONFIG" ]]; then
  CONFIG="config.example.json"
fi

# 规则书必须已经存在。这个脚本只负责“根据开端规划 + 写正文”
RULEBOOK_PATH="data/final/novel_rulebook.json"
if [[ ! -f "$RULEBOOK_PATH" ]]; then
  echo "错误：未找到规则书 $RULEBOOK_PATH" >&2
  echo "请先运行：python3 scripts/build_writer.py --stage rules" >&2
  exit 2
fi

# 第一步：根据开端生成/更新小说规划。
"$PYTHON_BIN" scripts/build_writer.py \
  --config "$CONFIG" \
  --run-mode "$RUN_MODE" \
  --stage plan \
  --project-id "$PROJECT_ID" \
  --opening "$OPENING" \
  --target-chapters "$TARGET_CHAPTERS" \
  --target-chars "$TARGET_CHARS"

# 第二步：根据已有规划逐章写正文。
"$PYTHON_BIN" scripts/build_writer.py \
  --config "$CONFIG" \
  --run-mode "$RUN_MODE" \
  --stage write \
  --project-id "$PROJECT_ID"
