然而，此时这位平日里风光无限的郡主，美眸中却压抑着冰冷的怒火。

“郡主，陛下……真的把您许配给那个刚下山的野道士了？”马车旁，贴身丫鬟翠儿一脸的愤愤不平，“奴婢听说，那人连件像样的道袍都没有，下山时还背着个破褡裢，里面装的都是烂红薯！”

萧梦瑶深吸了一口气，玉手紧紧攥着一方丝帕，冷声道：“圣旨已下，由不得本宫拒绝。据钦天监所言，本宫天生‘玄阴绝脉’，活不过十九岁。圣上听信妖言，说唯有青云山上‘纯阳观’的传人，方能以至阳之气为我续命。真是一派胡言！”

在这崇尚武道与儒术的大景朝，所谓的“修仙道士”，在权贵眼里不过是招摇撞骗的江湖骗子。

让堂堂郡主嫁给一个穷酸道士做贴身侍从兼名义上的夫君，这在整个京城已经成了一个天大的笑话。

