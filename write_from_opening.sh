林逸正毫无吃相地蹲在长凳上，稀溜溜地吸溜着一碗两文钱的素面。

他穿着一身洗得发白、甚至打了几个补丁的旧道袍，腰间挂着个看起来平平无奇的黄葫芦，一双贼溜溜的眼睛不时地往御街上过往的大家闺秀、小家碧玉身上瞟。

