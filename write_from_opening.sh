#!/usr/bin/env bash
set -euo pipefail

# 简单一键写小说脚本。
# 用法：改下面几个变量，然后执行：
#   ./write_from_opening.sh

PYTHON_BIN="${PYTHON_BIN:-python3}"
CONFIG="${CONFIG:-config.local.json}"
RUN_MODE="${RUN_MODE:-resume}"

# 输出项目名：结果会写到 data/projects/<PROJECT_ID>/
PROJECT_ID="${PROJECT_ID:-Urban-cultivation-in-ancient-times}"

# 小说开端：默认使用下面这段多行文本。
